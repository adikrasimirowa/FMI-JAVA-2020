/* This application displays the integer equivalents of some uppercase letters,
        lowercase letters, digits and special symbols. Display the integer equivalents of the
        following: A B C a b c 0 1 2 $ * + / and the blank character. */

package com.company;

public class Main {

    public static void main(String[] args) {

        System.out.println("A = " + (int) 'A');
        System.out.println("B = " + (int) 'B');
        System.out.println("C = " + (int) 'C');
        System.out.println("a = " + (int) 'a');
        System.out.println("b = " + (int) 'b');
        System.out.println("c = " + (int) 'c');
        System.out.println("0 = " + (int) '0');
        System.out.println("1 = " + (int) '1');
        System.out.println("2 = " + (int) '2');
        System.out.println("$ = " + (int) '$');
        System.out.println("* = " + (int) '*');
        System.out.println("+ = " + (int) '+');
        System.out.println("/ = " + (int) '/');
        System.out.println("blank char = " + (int) ' ');
    }
}
